package beans;

public class Funcionarios {

	//Atributos
	private String Colab;
	private double SalBruto;
	private double SalLiquido;
	private double ImpRenda;
	private String ValeTp;
	private String Cargo;
	
	
	//Getters and Setters
	public String getColab() {
		return Colab;
	}
	public void setColab(String colab) {
		Colab = colab;
	}
	public double getSalBruto() {
		return SalBruto;
	}
	public void setSalBruto(double salBruto) {
		SalBruto = salBruto;
	}
	public double getSalLiquido() {
		return SalLiquido;
	}
	public void setSalLiquido(double salLiquido) {
		SalLiquido = salLiquido;
	}
	public double getImpRenda() {
		return ImpRenda;
	}
	public void setImpRenda(double impRenda) {
		ImpRenda = impRenda;
	}
	public String getValeTp() {
		return ValeTp;
	}
	public void setValeTp(String valeTp) {
		ValeTp = valeTp;
	}
	public String getCargo() {
		return Cargo;
	}
	public void setCargo(String cargo) {
		Cargo = cargo;
	}
	
	
}
