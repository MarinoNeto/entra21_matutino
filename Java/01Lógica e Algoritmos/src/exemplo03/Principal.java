package exemplo03;

public class Principal {

	public static void main(String[] args) {
		
		//Variáveis
		int numero1=5, numero2=5; 
		
		//Operadores relacionais
		System.out.println(numero1 > numero2); //Maior
		System.out.println(numero1 < numero2); //Menor
		System.out.println(numero1 >= numero2); //Maior ou igual
		System.out.println(numero1 <= numero2);	//Menor ou igual
		System.out.println(numero1 == numero2); //Igual
		System.out.println(numero1 != numero2); //Diferente
		//System.out.println(numero1 === numero2); //Igualdade de valores e tipos
		
		
	}

}
